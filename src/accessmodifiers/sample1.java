package accessmodifiers;

public class sample1 {
	public void car() {
		System.out.println("car is printing");
		
	}
	
	public void mobile() {
		System.out.println("mobile is printing");
		
	}
	
	void clothes() {
		System.out.println("clothes are printing");
	}
	//it is imported in singleinheritance package
	protected void bike() {
		System.out.println("bike is printing");
		
	}

}
